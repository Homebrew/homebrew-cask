    sha256 "e09eca49d311b25f1b4023bcf379542a142c2a4f5ef957e8a478a2d4c957b327"
  else
    version "94.13.2.14503,893a1c9"
    sha256 "74122a729fd0421265b55bbd10475a7cfc634145c6c1b37bb93323eb44d82fe5"
  end

