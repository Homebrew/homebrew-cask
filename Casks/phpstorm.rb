  arch arm: "-aarch64"

  version "2023.1.4,231.9225.18"
  sha256 arm:   "3285135fc4c529640ecfc5b451fa9b51d9df2a323915509cc6cbb3f25717c9e2",
         intel: "4d3d9005772d2136e44f7774377fae053b690501800ea5e650d0f35882690fdd"

