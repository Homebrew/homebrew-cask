  arch arm: "-aarch64"

  version "2023.2.4,232.10227.13"
  sha256 arm:   "7ba3c7573b478ae004e296af4ddf8fbdf05b8453c41a06675b1a2350990f09b0",
         intel: "cba432cf0a29e55a92fb89daa58f27f590c79e21ad3d46cd37b1da2c9a10ade7"

