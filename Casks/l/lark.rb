  livecheck_arch = on_arch_conditional arm: "_m1"

  on_arm do
    version "7.70.12,499fe423"
    sha256 "3096ec02e49fe9f30aadc22d3bedf8c33a6f5c5f09b11a98a7bbdd99e93ddba1"

