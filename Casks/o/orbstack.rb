cask "orbstack" do
  arch arm: "arm64", intel: "amd64"

  version "1.0.0_16230"
  sha256 arm:   "e1e7cfa0b5251fac62aa427c8c82222d841d4f2ee9cd6de9cc85c9cf2626fa17",
         intel: "c89c6e9eeb1f33a502f2f034d08d6a4edfa86edf24d2abdc96e3f326cc1096c8"

