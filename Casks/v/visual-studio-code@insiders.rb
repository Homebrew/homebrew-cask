cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.108.0-insider,1765887033188,fd1cf0488d736dbc86c6395c783c31992cce8da4"
  sha256 arm:   "25544d58ec2083150b6b01e5636ec620353c40754b4a4fd95a07991f154c6ba3",
