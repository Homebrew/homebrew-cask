  arch arm: "-arm64"

  version "1.112.0-insider,1773444732638,4c822235d64e2486552fd52187d1b2a29fa8f4f1"
  sha256 arm:   "bfbb2b09f19f3db6f62bf4f6c9c00da6e7e83ea105670574d092c49564b1f926",
