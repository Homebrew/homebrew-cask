  arch arm: "-arm64"

  version "1.106.0-insider,1762491878377,97da3098581a5fe4e4d20a86eaff1f6e3ae1a4ae"
  sha256 arm:   "150a906da743cdf8363e253bd1f450f1ef1b2f433c298c80d78ec15f43539aa0",
