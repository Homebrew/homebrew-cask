  arch arm: "-arm64"

  version "1.109.0-insider,1769760094331,a1728b98fa654220245c84bd92bb1baf13f730c6"
  sha256 arm:   "ecc8b162c960b938e133346e1307daffa989021d75269da237b861187319cb94",
