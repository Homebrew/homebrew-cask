cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.115.0-insider,1775599880418,41dd792b5e652393e7787322889ed5fdc58bd75b"
  sha256 arm:   "08b21ee959c23d16a226de3485a3347da7e49f946add0b0c9b57efff4596bbc5",
