cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.113.0-insider,1773939998238,7a43958ba42a5fcb5455f068c5a0a7e322dc4532"
  sha256 arm:   "578b117f31435a4cee5b9e340de0d9adbefe32110ce62436db281cb3fa39a0ba",
