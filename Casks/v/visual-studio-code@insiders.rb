cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.116.0-insider,1775670445365,fc6d18c3174aa8962d90435aba5b51ef6c300b9a"
  sha256 arm:   "d55c5e8ad0584191ea7c325bed597c10d809204799ed46b81c0d122b15b4a0d0",
