  arch arm: "-arm64"

  version "1.106.0-insider,1762319059091,1a756d609ca6220dbd315a779b9ef1baf8a761f2"
  sha256 arm:   "ec548cb70221aea24325d1c8ed28732a936d68936d222aa5e1a556bf57f1ace0",
