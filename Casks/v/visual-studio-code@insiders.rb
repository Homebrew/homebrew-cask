  arch arm: "-arm64"

  version "1.106.0-insider,1762405462317,402271f5deb7fc29b000463cfa525fc8c03fb7d7"
  sha256 arm:   "5975c03475ce3736a349999125a4090bfbe021c506aebff749d3c1403d8ba25e",
