cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.103.0-insider,1752728695850,344abe8f7ce560f4883e1542669dd96e5118c767"
  sha256 arm:   "9da35cd10b23c8288cf7a92fc1079daaceabcb788886bdbf15250f14c1841082",
