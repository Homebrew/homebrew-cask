  arch arm: "-arm64"

  version "1.101.0-insider,1747026471756,5d3e84d3aaaea9e132bad476f1be5a4846289b75"
  sha256 arm:   "f64b874dc6b8187463c7ca8016bb36c772696d5d6796f0e8a55948cee38b04b2",
