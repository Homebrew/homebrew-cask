  arch arm: "-arm64"

  version "1.109.0-insider,1768516518367,1656700d1db06e50d22e733b890f2d29c23f70d9"
  sha256 arm:   "5ca49d8e04b9acb0c3987eac035f85eecefa646b344ce707fe0d7f6df43ce53e",
