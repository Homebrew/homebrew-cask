cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.112.0-insider,1773679401718,adbfed6309efb59cd0db106982bbd39a97125e68"
  sha256 arm:   "4380906f14ce51f80f5f53a9fcd9b1601697ea4f14a682831e6626461e9902f4",
