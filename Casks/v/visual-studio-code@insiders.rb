  arch arm: "-arm64"

  version "1.106.0-insider,1761887073451,e637e2ef735545f15caaeb95cfa6c5998dab8124"
  sha256 arm:   "b49f179615c349189deb85944fdb199a3fecdc8b717f3f03be597c8eb26ec779",
