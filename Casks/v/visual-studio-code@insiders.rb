  arch arm: "-arm64"

  version "1.106.0-insider,1762814505459,507f50c4289e1f7c154a8129c5b0122a671cf038"
  sha256 arm:   "548840a17095464da300be81df80f0067f7ce751b8fc8ab2606bad191e3b1fa0",
