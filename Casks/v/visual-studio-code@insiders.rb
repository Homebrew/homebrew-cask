cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.110.0-insider,1771477720992,243a432d167fe650b6afc83563bb704f9233c682"
  sha256 arm:   "dde4005a6b056f284c9a8958c34f3d176032caa729d960f4919f3bda622af307",
