cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.109.0-insider,1768799174798,6b921dda5c679c6ca1928a4505567c533ede72f9"
  sha256 arm:   "6a77a90aa82ba2de702b0d70425cf70e2927490acb46e7725ce01698850d2cc1",
