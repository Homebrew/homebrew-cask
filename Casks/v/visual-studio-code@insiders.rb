cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.115.0-insider,1775531611652,d09c516f0dbb4c2992964976425b1926ccda08ad"
  sha256 arm:   "27087790d98b402bf646dd4f6799c276ae9c8e04e22773ec4dc057832bf99345",
