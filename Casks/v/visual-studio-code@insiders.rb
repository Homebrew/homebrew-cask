cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.99.0-insider,1742456580841,90dde06fb64726923ebef8121bd63836529b7d8b"
  sha256 arm:   "26a5435c70aed054d19cc5160d5ff0a3cbfb02600ecbda1a22c654d74e70344a",
