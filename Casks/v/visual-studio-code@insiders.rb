cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.100.0-insider,1745903186460,ecbbe26188235072fbde7b6f4fd2a0bd6a7c9909"
  sha256 arm:   "69464742dc7e3e1dde04a163900d13a211f9bb4700c8698c5b49bea978f3625a",
         intel: "38931ffef51a7d67288676a7497d4518110b917df9f86652f29d78f4f1a56c77"

  url "https://vscode.download.prss.microsoft.com/dbazure/download/insider/#{version.csv.third}/VSCode-darwin#{arch}.zip",
