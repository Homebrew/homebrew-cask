cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.108.0-insider,1766153666395,7f08f95ad54782bd242f5536470b330282197333"
  sha256 arm:   "94a973f6fd769fba6a8157d012bbcc5979c98e444a4198563de2263892e5c059",
