  arch arm: "-arm64"

  version "1.102.0-insider,1751432718666,6e855004c713d9c2610091ab7eb68b677830087f"
  sha256 arm:   "f74be56b54dd496af76c5266c90eeca98b6327acfd3f5c405751a83f01610204",
