cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.108.0-insider,1765429416789,5dab7809de7d46dffea7180dddcde7100f2ab4d6"
  sha256 arm:   "beb9ca70673ee159429c0ab537c8f9722c7b9dbc1c5545c8318a436a44860a39",
