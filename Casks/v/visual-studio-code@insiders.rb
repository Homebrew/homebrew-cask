  arch arm: "-arm64"

  version "1.107.0-insider,1763045225948,769ef65cd62212cde4db47c538acfea3063f1277"
  sha256 arm:   "c0d02ddd541f512db74f3839114e4152bff667447590697bfa881f699288b35d",
