cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.108.0-insider,1765446092555,01586ca5669ef4506d22ddc05b55dd9892571a42"
  sha256 arm:   "e01b9035c917a2e28beac5002b1b2e5f562cb497afdfd861f6e2392f9dff264b",
