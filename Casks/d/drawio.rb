cask "drawio" do
  arch arm: "arm64", intel: "x64"

  version "29.3.6"
  sha256 arm:   "f2ba2218e7956e42c49bd80edb19278f9209e4fa0c1016a827e116ecf391a64b",
         intel: "9b42a1d6ffd7a36e61567654760edf42a17965fc68eb620fd391c97bb8c65b14"

