  livecheck_arch = on_arch_conditional arm: "_m1"

  on_arm do
    version "7.69.9,b258db78"
    sha256 "b658f4029e3cccd892da078706c947f671a4e01d83a5450a6c9a8bc75a03f1a5"
  end
